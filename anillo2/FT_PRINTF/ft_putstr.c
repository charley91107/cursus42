#include "ft_printf.h"

int ft_putstr(char *s)
{
    size_t i;
    
    i = 0;
    if (!s)
        return (ft_putchar("null"));
    i = 0;
	while (s[i])
	{
		write(1, s[i], 1);
		i++;
	}
    return (i);
}
